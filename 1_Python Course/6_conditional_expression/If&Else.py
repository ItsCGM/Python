# If, Else and Elif in python are used to perform a certain conditions.

#Creating a If, Else & Elif statements.
a = int(input("enter your age: "))
if a>=18:
    print("you are eligible to vote")
elif a<1:
    print("you have entered the invalid age")
else:
    print("you are not eligible to vote")


#if & elif can also be created in a nested form.